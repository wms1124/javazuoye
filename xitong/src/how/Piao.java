package how;

public class Piao {

	private int id ;//目的地名称
	
	private int piaof;//票价
	
	private int piaonum;//票数

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public int getPiaof() {
		return piaof;
	}

	public void setPiaof(int piaof) {
		this.piaof = piaof;
	}

	public int getPiaonum() {
		return piaonum;
	}

	public void setPiaonum(int piaonum) {
		this.piaonum = piaonum;
	}

	
}
